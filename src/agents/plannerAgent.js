function summarizeBlueprint(blueprint) {

  const dbSchema = blueprint.dbSchema || {};
  const dependencies = blueprint.dependencies || {};

  return {
    databaseType: dbSchema.databaseType,

    entities: summarizeEntities(blueprint.entities),
    tables: summarizeTables(dbSchema.tables),

    apiEndpoints: summarizeApiEndpoints(blueprint.apiEndpoints),
    frontendPages: summarizeFrontendPages(blueprint.frontendPages),
    folderStructure: blueprint.folderStructure,

    backendDeps: getDependencyNames(dependencies.backend),
    frontendDeps: getDependencyNames(dependencies.frontend),
  };
}

function summarizeEntities(entities = []) {
  return entities.map((entity) => ({
    name: entity.name,
    tableName: entity.tableName,
    apiPath: entity.apiPath,
    modelFile: entity.modelFile,
    routeFile: entity.routeFile,
  }));
}

function summarizeTables(tables = []) {
  return tables.map((table) => ({
    name: table.name,
    fieldCount: table.fields?.length || 0,
    foreignKeys: table.foreignKeys?.map((fk) => fk.references) || [],
  }));
}

function summarizeApiEndpoints(apiEndpoints = []) {
  return apiEndpoints.map((endpoint) => ({
    method: endpoint.method,
    path: endpoint.path,
    relatedTable: endpoint.relatedTable,
    requiresAuth: Boolean(endpoint.requiresAuth),
  }));
}

function summarizeFrontendPages(frontendPages = []) {
  return frontendPages.map((page) => ({
    name: page.name,
    route: page.route,
    componentCount: page.components?.length || 0,
  }));
}

function getDependencyNames(packageSection = {}) {
  return Object.keys(packageSection.dependencies || {});
}


export async function plannerAgentNode(state) {

  console.log("\n [Planner Agent] Creating build plan...\n");

  const { blueprint, clarifiedSpec } = state;

  const blueprintSummary = summarizeBlueprint(blueprint);

  const result = await safeCallGemini({
    systemPrompt: PLANNER_PROMPT,
    userPrompt: `This is the app name: ${clarifiedSpec.appName}\n\nThis is the blueprint summary:\n${JSON.stringify(blueprintSummary, null, 2)}\n\nUse this project specification as the source of truth:\n${JSON.stringify(clarifiedSpec, null, 2)}`,
    agentName: "plannerAgent",
  });


  if (!result.ok) {
    return { error: `plannerAgent failed: ${result.error}` };
  }

  const plan = result.parsed;

  return {
    taskQueue: plan,
    currentPhaseIndex: 0,
    currentTaskIndex: 0,
  };
}
